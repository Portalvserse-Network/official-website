<script>
import SplitType from 'split-type'

export default {
	name: 'IndexPage',
	transition: {
		name: 'IndexPage',
		appear: true,
		css: true,
		enter(el, done) {
			// Title definitions

			const about = new SplitType('#about', {
				types: 'words',
			})

			const buttons = document.querySelectorAll('.button')
			const blooms = document.querySelectorAll('.bloom')
			const control1 = document.getElementById('controler1')
			const control2 = document.getElementById('controler2')

			// console.log(cards)
			// animation timeline for headers
			this.headlineTimeLine = this.$gsap.timeline({ onComplete: done })
			this.headlineTimeLine
				.from(about.words, {
					opacity: 0,
					duration: 1,
					y: '-20%',
					stagger: { amount: 0.2 },

					ease: 'power2.out',
				})
				// .from(blooms, {
				// 	autoAlpha: 0,
				// 	y: '-10%',
				// 	duration: 1,
				// 	ease: 'power2.out',
				// })
				.from(
					buttons,
					{
						autoAlpha: 0,
						y: '-5%',
						duration: 1,
						stagger: { amount: 0.2 },
						ease: 'power2.out',
					},
					'<'
				)
				.from(
					control1,
					{
						opacity: 0,
						x: '-10%',
						duration: 1,
						stagger: { amount: 0.2 },
						ease: 'expo',
					},
					'<'
				)
				.from(
					control2,
					{
						opacity: 0,
						x: '10%',
						duration: 1,
						stagger: { amount: 0.2 },
						ease: 'expo',
					},
					'<'
				)

			// registering every project with a scroll trigger
			blooms.forEach((bloom) => {
				this.$gsap
					.timeline({
						scrollTrigger: {
							trigger: bloom,
							start: 'top center+=1%',
							once: true,
							// markers: true,
						},
					})
					.from(bloom, {
						// y: '-10%',
						autoAlpha: 0,
						duration: 1,
						ease: 'power2.out',
						stagger: { amount: 0.4 },
					})
			})
		},
		leave(el, done) {},
	},
	data() {
		return {
			headlineTimeLine: null,
		}
	},
}
</script>

